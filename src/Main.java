    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultTableModel dtm=(DefaultTableModel) tblAll.getModel();
        int row=tblAll.getSelectedRow();
        int id=(int) dtm.getValueAt(row, 0);
        double rating=(double) dtm.getValueAt(row, 3);
        try {
            ArrayList<Student> students=ReviewController.getEligibleList(id);
            DefaultTableModel dtmElg=(DefaultTableModel) tblEligible.getModel();
            dtmElg.setRowCount(0);
            for (Student st : students) {
                dtmElg.addRow(new Object[]{st.getId(),st.getFirstName(),st.getSecondName(),st.getRating(),st.getLastContact()});
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            Review r=ReviewController.getStudentToReview(id, rating, 1);
            txtID.setText(r.getStID()+"");
            txtSub.setText(r.getSubID()+"");
            txtFirst.setText(r.getFirstName());
            txtSecond.setText(r.getSecondName());
            txtRating.setText(r.getRating()+"");
            txtCont.setText(r.getLastCont());
            txtFeedBacker.setText(id+"");
            DefaultTableModel dtmRes=(DefaultTableModel) tblReserved.getModel();
            dtmRes.addRow(new Object[]{r.getStID(),r.getSubID(),r.getFirstName(),r.getSecondName(),r.getRating(),r.getLastCont(),id});
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

