    private void fillAllStudentTable() {
        DefaultTableModel dtm=(DefaultTableModel) tblAll.getModel();
        dtm.setRowCount(0);

        ArrayList<Student> students=null;
        try {
            students = ReviewController.getAllSubmittedStudents();
            for (Student st : students) {
            dtm.addRow(new Object[]{st.getId(),st.getFirstName(),st.getSecondName(),st.getRating(),st.getLastContact()});
        }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
