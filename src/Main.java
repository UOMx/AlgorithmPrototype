    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String feedBacker=txtFeedBacker.getText();
        String submission=txtSub.getText();
        try {
            ReviewController.saveReview(Integer.parseInt(feedBacker), "", "", "", "", Integer.parseInt(submission));
            int subID=Integer.parseInt(txtSub.getText());
            DefaultTableModel dtm=(DefaultTableModel) tblReserved.getModel();
            int rowCount=dtm.getRowCount();
            for (int i = 0; i < rowCount; i++) {
                if((int)dtm.getValueAt(i, 1)==subID){
                    dtm.removeRow(i);
                    break;
                }
            }
            txtID.setText("");
            txtSub.setText("");
            txtFirst.setText("");
            txtSecond.setText("");
            txtRating.setText("");
            txtCont.setText("");
            txtFeedBacker.setText("");
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

